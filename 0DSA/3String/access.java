public class access {
    public static void main(String[] args) {
        String str = "Hello, World!";
        char ch = str.charAt(7); // Accessing the character at index 7
        System.out.println("The character at index 7 is: " + ch);
    }
}


//String is Immutable in Java.
//Once a String object is created, it cannot be changed. 
// You can not update the value of a String object, 
// but you can create a new String object with the updated value.